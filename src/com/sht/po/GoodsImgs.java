package com.sht.po;

public class GoodsImgs {
    private String id;

    private String owner;

    private String img;

    private Short main;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public Short getMain() {
        return main;
    }

    public void setMain(Short main) {
        this.main = main;
    }
}